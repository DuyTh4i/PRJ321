    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        request.setCharacterEncoding("UTF-8");
        String code = request.getParameter("code");
        productDAO pdb = new productDAO();
        Product p = pdb.getProductByCode(code);
        request.setAttribute("p", p);
        request.getRequestDispatcher("edit_product.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("name");
        String character = request.getParameter("character");
        int price = Integer.parseInt(request.getParameter("price"));
        String date = request.getParameter("date");
        String describe = character+"_"+request.getParameter("describe")+"_Good Smile Company_10cm";
        int code = Integer.parseInt(request.getParameter("code"));
        productDAO pdb = new productDAO();
        pdb.update(code, name, price, describe, date);
        request.getRequestDispatcher("detail?code="+code).forward(request, response);
    }

